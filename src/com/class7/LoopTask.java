package com.class7;

public class LoopTask {

	public static void main(String[] args) {
		
		
		//Task 1 --> print only odd number from 50 to 100
	
				
				
				int x=50;
				
				while(x<=100) {
					if(x%2!=0) {
						System.out.println(x);
		
					}
					x++;
				}
				
				
		//Task 2--> print only even number from 100 to 1
				
				int y=100;
				
				while(y>=1) {
					if(y%2==0) {
						System.out.println(y);
						
					}
					
					y--;
				}

	}

}
