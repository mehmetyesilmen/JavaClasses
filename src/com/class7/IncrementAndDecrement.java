package com.class7;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		
		int a=1;//assignment
		a=a+1;//shorthand
		System.out.println(a);
		a++;//increment
		System.out.println(a);
		
		int b=4;//assignment
		b=b-1;//shorthand
		System.out.println(b);
		b--;//decrement
		System.out.println(b);
		
		
		//increment & decrement operators can be used 
	    //only with variables;
		
		int c=23;
		c++;
		//1++; compiler error invalid argument


		        
	}

}
